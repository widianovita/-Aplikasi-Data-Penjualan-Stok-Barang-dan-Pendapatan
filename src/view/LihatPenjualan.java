    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
         if (listPenjualan.length>0)
        { 
            daftarPenjualanDefaultTableModel.setRowCount(0); 
            for (int i=0; i<listPenjualan.length; i++)
            { 
                daftarPenjualanDefaultTableModel.insertRow( daftarPenjualanDefaultTableModel.getRowCount(),listPenjualan[i]); 
            } 
        } 
        else 
        { 
            dispose();
        }
    }//GEN-LAST:event_formWindowActivated

